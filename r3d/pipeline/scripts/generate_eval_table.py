"""Generate the eval results table from scored eval_report.json files.

Reads eval_report.json files for each model configuration and produces
the formatted tables for eval_results.txt, including per-group averages
(MC, measurement, distance, volume) and overall.

Usage:
    python -m r3d.pipeline.scripts.generate_eval_table \
      --reports "R3D 8B:/tmp/scores_8b/eval_report.json" \
                "R3D 35B:/tmp/scores_35b/eval_report.json" \
      --output /tmp/eval_results.txt
"""

from __future__ import annotations

import argparse
import json
from pathlib import Path

MC_TYPES = [
    "gap_fit",
    "nearest_from_set",
    "top_higher",
    "which_taller",
    "which_longer_dim",
]
DISTANCE_TYPES = [
    "global_how_far",
    "global_how_far_from_me",
    "global_how_long",
    "how_much_taller",
    "how_much_longer_dim",
    "total_fly_distance",
    "total_walk_distance",
]
VOLUME_TYPES = ["volume_estimation", "pour_leftover", "pour_room_left"]

ALL_MRA_TYPES = DISTANCE_TYPES + VOLUME_TYPES
ALL_TYPES = MC_TYPES + ALL_MRA_TYPES

COL_WIDTH = 14


def _load_report(path: str) -> dict:
    with open(path) as f:
        return json.load(f)["aggregate"]


def _fmt(val: float | None, suffix: str = "%") -> str:
    if val is None:
        return "—".center(COL_WIDTH)
    return f"{val:.1f}{suffix}".rjust(COL_WIDTH)


def _get_metric(agg: dict, qt: str, metric: str) -> float | None:
    d = agg.get(qt)
    if d is None:
        return None
    return d.get(metric)


def _group_avg(agg: dict, qtypes: list[str], metric: str) -> float | None:
    vals = [_get_metric(agg, qt, metric) for qt in qtypes]
    vals = [v for v in vals if v is not None]
    if not vals:
        return None
    return sum(vals) / len(vals)


def _build_table(
    columns: list[tuple[str, dict]],
    qtypes: list[str],
    metric: str,
    groups: list[tuple[str, list[str]]],
    title: str,
    separator_after: list[str] | None = None,
) -> list[str]:
    label_width = 30
    sep = "-" * (label_width + COL_WIDTH * len(columns))

    lines = [title, ""]
    header = " " * label_width + "".join(name.rjust(COL_WIDTH) for name, _ in columns)
    lines.append(header)
    lines.append(sep)

    for qt in qtypes:
        line = qt.ljust(label_width)
        for _, agg in columns:
            val = _get_metric(agg, qt, metric)
            line += _fmt(val)
        lines.append(line)
        if separator_after and qt in separator_after:
            lines.append(sep)

    lines.append(sep)

    for group_name, group_qtypes in groups:
        line = group_name.ljust(label_width)
        for _, agg in columns:
            avg = _group_avg(agg, group_qtypes, metric)
            line += _fmt(avg)
        lines.append(line)

    return lines


def main() -> None:
    parser = argparse.ArgumentParser(description="Generate eval results table")
    parser.add_argument(
        "--reports",
        type=str,
        nargs="+",
        required=True,
        help="Pairs of 'label:path' for each model's eval_report.json",
    )
    parser.add_argument("--output", type=str, required=True)
    args = parser.parse_args()

    columns = []
    for spec in args.reports:
        label, path = spec.split(":", 1)
        agg = _load_report(path)
        columns.append((label, agg))

    lines = [
        "R3D Evaluation Results",
        "(generated by r3d.pipeline.scripts.generate_eval_table)",
        "",
    ]

    # Table 1: Accuracy (MC types)
    lines += _build_table(
        columns,
        MC_TYPES,
        "mean_accuracy",
        [("MC avg", MC_TYPES)],
        "Table 1: Accuracy — Multiple Choice (higher = better)",
    )
    lines.append("")

    # Table 2: MRA
    lines += _build_table(
        columns,
        ALL_MRA_TYPES,
        "mra",
        [("Distance avg", DISTANCE_TYPES), ("Volume avg", VOLUME_TYPES)],
        "Table 2: MRA — Mean Relative Accuracy (higher = better)",
        separator_after=["total_walk_distance"],
    )
    label_width = 30
    line_overall = "OVERALL (all 15)".ljust(label_width)
    for _, agg in columns:
        acc_vals = [_get_metric(agg, qt, "mean_accuracy") for qt in MC_TYPES]
        mra_vals = [_get_metric(agg, qt, "mra") for qt in ALL_MRA_TYPES]
        all_vals = [v for v in acc_vals + mra_vals if v is not None]
        line_overall += _fmt(sum(all_vals) / len(all_vals) if all_vals else None)
    lines.append(line_overall)
    lines.append("")

    # Table 3: Within threshold
    lines.append("Table 3: Acc <5% (dist) / <25% (vol) (higher = better)")
    lines.append("")
    sep = "-" * (label_width + COL_WIDTH * len(columns))
    header = " " * label_width + "".join(name.rjust(COL_WIDTH) for name, _ in columns)
    lines.append(header)
    lines.append(sep)

    for qt in DISTANCE_TYPES:
        line = qt.ljust(label_width)
        for _, agg in columns:
            line += _fmt(_get_metric(agg, qt, "within_5_pct"))
        lines.append(line)
    lines.append(sep)
    for qt in VOLUME_TYPES:
        line = qt.ljust(label_width)
        for _, agg in columns:
            line += _fmt(_get_metric(agg, qt, "within_25_pct"))
        lines.append(line)
    lines.append(sep)

    line = "Distance avg".ljust(label_width)
    for _, agg in columns:
        avg = _group_avg(agg, DISTANCE_TYPES, "within_5_pct")
        line += _fmt(avg)
    lines.append(line)

    line = "Volume avg".ljust(label_width)
    for _, agg in columns:
        avg = _group_avg(agg, VOLUME_TYPES, "within_25_pct")
        line += _fmt(avg)
    lines.append(line)

    line = "OVERALL".ljust(label_width)
    for _, agg in columns:
        w5 = [_get_metric(agg, qt, "within_5_pct") for qt in DISTANCE_TYPES]
        w25 = [_get_metric(agg, qt, "within_25_pct") for qt in VOLUME_TYPES]
        all_vals = [v for v in w5 + w25 if v is not None]
        line += _fmt(sum(all_vals) / len(all_vals) if all_vals else None)
    lines.append(line)
    lines.append("")

    # Table 4: Mean % Error
    lines += _build_table(
        columns,
        ALL_MRA_TYPES,
        "mean_percentage_error",
        [
            ("Distance avg", DISTANCE_TYPES),
            ("Volume avg", VOLUME_TYPES),
            ("OVERALL", ALL_MRA_TYPES),
        ],
        "Table 4: Mean % Error (lower = better)",
        separator_after=["total_walk_distance"],
    )
    lines.append("")

    # Summary
    lines.append("Summary")
    lines.append("")
    lines.append(
        " " * label_width + "".join(name.rjust(COL_WIDTH) for name, _ in columns)
    )
    lines.append(sep)
    line_n = "N scored".ljust(label_width)
    line_pf = "Parse fail".ljust(label_width)
    for _, agg in columns:
        n = sum(agg[qt]["total"] for qt in ALL_TYPES if qt in agg)
        pf = sum(agg[qt]["parse_failures"] for qt in ALL_TYPES if qt in agg)
        line_n += str(n).rjust(COL_WIDTH)
        line_pf += str(pf).rjust(COL_WIDTH)
    lines.append(line_n)
    lines.append(line_pf)

    output = "\n".join(lines) + "\n"
    Path(args.output).parent.mkdir(parents=True, exist_ok=True)
    with open(args.output, "w") as f:
        f.write(output)
    print(f"Wrote {args.output}")
    print(output)


if __name__ == "__main__":
    main()
